public final class LookupSwitchTest {
    public static void main(String[] args) {
        for (int i = 0; i < 2; i++) {
            switch (i) {
                case 0:
                    System.out.println("0 case");
                    break;
                case 1:
                    System.out.println("1 case");
            }
        }
    }
}